function MarketPage() {
  const [data, setData] = useState(null);
  const [loading, setLoading] = useState(true);
  const [error, setError] = useState(null);

  useEffect(() => {
    fetch("/api/feargreed")
      .then((r) => r.json())
      .then((d) => {
        if (d.error) throw new Error(d.error);
        setData(d);
      })
      .catch((e) => setError(e.message))
      .finally(() => setLoading(false));
  }, []);

  if (loading)
    return (
      <Box
        sx={{
          flex: 1,
          display: "flex",
          alignItems: "center",
          justifyContent: "center",
        }}
      >
        <CircularProgress />
      </Box>
    );

  if (error || !data)
    return (
      <Box
        sx={{
          flex: 1,
          display: "flex",
          alignItems: "center",
          justifyContent: "center",
        }}
      >
        <Typography color="danger" level="body-md">
          {error ?? "No data available"}
        </Typography>
      </Box>
    );

  const updated = new Date(data.timestamp).toLocaleString(undefined, {
    month: "short",
    day: "numeric",
    hour: "2-digit",
    minute: "2-digit",
  });

  return (
    <Box sx={{ flex: 1, overflow: "auto", p: 3 }}>
      <Box sx={{ maxWidth: 860, mx: "auto" }}>
        {/* Page header */}
        <Box
          sx={{
            display: "flex",
            alignItems: "baseline",
            justifyContent: "space-between",
            mb: 3,
          }}
        >
          <Box>
            <Typography level="h3">Market Overview</Typography>
            <Typography level="body-sm">
              Fear &amp; Greed Index — pick the style you prefer
            </Typography>
          </Box>
          <Box sx={{ display: "flex", alignItems: "center", gap: 1 }}>
            {data.stale && (
              <Chip size="sm" color="warning" variant="soft">
                Stale
              </Chip>
            )}
            <Typography level="body-xs" textColor="neutral.400">
              Updated {updated}
            </Typography>
          </Box>
        </Box>

        {/* Both visualizations side by side */}
        <Box
          sx={{
            display: "grid",
            gridTemplateColumns: "1fr 1fr",
            gap: 2,
            mb: 3,
          }}
        >
          <FearGreedBar score={data.score} />
          <FearGreedGauge score={data.score} />
        </Box>

        {/* Component breakdown */}
        <Sheet variant="outlined" sx={{ borderRadius: "lg", p: 3 }}>
          <Typography
            level="body-xs"
            textColor="neutral.500"
            sx={{ letterSpacing: "0.1em", fontWeight: 700, mb: 1.5 }}
          >
            COMPONENTS
          </Typography>
          {data.components.map((c) => (
            <ComponentRow
              key={c.name}
              name={c.name}
              val={c.val}
              desc={c.desc}
              raw={c.raw}
            />
          ))}
        </Sheet>
      </Box>
    </Box>
  );
}

export default MarketPage;
