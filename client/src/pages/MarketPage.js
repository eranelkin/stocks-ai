// ── Zone helper ───────────────────────────────────────────────────────────────

function getZone(score) {
  if (score <= 25) return { label: "Extreme Fear", color: "#ef5350" };
  if (score <= 45) return { label: "Fear", color: "#ff9800" };
  if (score <= 55) return { label: "Neutral", color: "#fdd835" };
  if (score <= 75) return { label: "Greed", color: "#66bb6a" };
  return { label: "Extreme Greed", color: "#43a047" };
}

